bool MEDIA::Open() {
	if(!Opened) {
		Opened = true;
		return true;
	}
	return false;
}
/*--------------------------------------------------------------------------------------------------------------------*/
void MEDIA::Close() {
	Opened = false;
}
/*--------------------------------------------------------------------------------------------------------------------*/
bool MEDIA::IsOpen() {
	return Opened;
}  
